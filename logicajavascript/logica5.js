// Adição

function adicao() {
    var adicao1 = 5;
    var adicao2 = 5;
    var resultado;

    resultado = adicao1 + adicao2
    console.log("O resultado é: ", resultado)
    }
    adicao();

// Utilização de parametros e retorno

function adicao(valor1, valor2) {
    return valor1 + valor2;
}
console.log('Resultado = ', adicao(5, 5));

