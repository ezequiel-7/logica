// Adição

function adicao() {
    var adicao1 = 5;
    var adicao2 = 5;
    var resultado;

    resultado = adicao1 + adicao2
    console.log("O resultado é: ", resultado)
    }
    adicao();

// Utilização de parametros e retorno

function adicao7(adicao8, adicao9) {
    return adicao8 + adicao9;
}
console.log('Resultado = ', adicao7(5, 5));

